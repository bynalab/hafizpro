List<JuzModel> searchJuz(String query) {
  final trimmed = query.trim();
  if (trimmed.isEmpty) {
    return _juzList;
  }

  final q = trimmed.toLowerCase();
  final asInt = int.tryParse(trimmed);

  return _juzList.where((juz) {
    if (asInt != null) {
      return juz.number == asInt;
    }
    return juz.name.toLowerCase().contains(q);
  }).toList();
}
